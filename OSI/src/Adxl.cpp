#include "Adxl.h"
// ======================================================
// CONSTRUCTOR
// ======================================================
Adxl::Adxl(IAdxlTransport* adxlTrans){
    // ==================================================
    // salvează pointerul către transport
    // dependency injection
    // ==================================================
    this->adxlTrans = adxlTrans;
}
// ======================================================
// INITIALIZARE
// ======================================================
void Adxl::init()
{
    adxlTrans->init();
}
// ======================================================
// PORNIRE MĂSURARE
// ======================================================
void Adxl::start() 
{
    // ==================================================
    // registrul 0x2D = POWER_CTL
    // bit3 = Measure
    // Formula:
    // (1 << 3)
    // deplasează valoarea 1
    // pe poziția bitului 3
    // ==================================================
    char c = 1 << 3;
    // ==================================================
    // scrie în registrul POWER_CTL
    // ==================================================
    adxlTrans->write(0x2D, &c, 1);
}
// ======================================================
// VERIFICARE CONECTARE
// ======================================================
bool Adxl::isConnected()
{
    char id;
    // ==================================================
    // registrul 0x00 = DEVICE_ID
    // ==================================================
    adxlTrans->read(0x00, &id, 1);
    return id == (char)0xe5;
}
// ======================================================
// CITIRE X Y Z
// ======================================================
void Adxl::dataReader(int &X, int &Y, int &Z)
{
    // ==================================================
    // fiecare axă:
    // 16 biți
    // X0 X1
    // Y0 Y1
    // Z0 Z1
    // ==================================================
    char id[6];
    // ==================================================
    // registrul 0x32 = DATAX0
    // ==================================================
    adxlTrans->read(0x32, id, 6);
    // ==================================================
    // reconstruire valori pe 16 biți
    // Formula:
    // HIGH_BYTE << 8
    // deplasează byte-ul superior
    // pe pozițiile corecte
    // ==================================================
    X = (id[0] << 8) | (id[1] & 0xFF);
    Y = (id[2] << 8) | (id[3] & 0xFF);
    Z = (id[4] << 8) | (id[5] & 0xFF);
}
